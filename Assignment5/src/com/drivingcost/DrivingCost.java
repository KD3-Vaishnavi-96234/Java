package com.drivingcost;

public class DrivingCost {
	
	double totalMiles;
	double costOfGasoline;
	double avgMiles;
	double parkingFees;
	Double toll;
	
	public DrivingCost(double totalMiles, double costOfGasoline, double avgMiles, double parkingFees, Double toll) {
		super();
		this.totalMiles = totalMiles;
		this.costOfGasoline = costOfGasoline;
		this.avgMiles = avgMiles;
		this.parkingFees = parkingFees;
		this.toll = toll;
	}
	
}
